  public static AiTestVectorStoreResource INSTANCE;

  private final Map<String, VectorEntity> entities = new ConcurrentHashMap<>();
  private float threshold;
  public boolean foundRelevant = false;

  public AiTestVectorStoreResource() {
    entities.put("1", new VectorEntity("1", embedding, Map.of()));
    if (INSTANCE == null) {
      INSTANCE = this;
    }
  }

  @Override
  protected void doStart() throws Exception {
    super.doStart();
    threshold = configuration().properties().threshold();
  }

  @Override
  public Completable add(VectorEntity vectorEntity) {
    return Completable.fromRunnable(() ->
      entities.put(vectorEntity.id(), vectorEntity)
    );
  }

  @Override
  public Flowable<VectorResult> findRelevant(VectorEntity vectorEntity) {
    return Flowable
      .fromIterable(entities.values())
      .flatMap(v -> {
        float score = computeScore(vectorEntity, v);
        if (score > threshold) {
          INSTANCE.foundRelevant = true;
          return Flowable.just(new VectorResult(v, score));
        }
        return Flowable.empty();
      });
  }

  private static float computeScore(VectorEntity vectorEntity, VectorEntity v) {
    return NativeMath.INSTANCE.cosineScore(v.vector(), vectorEntity.vector());
  }

  @Override
  public void remove(VectorEntity vectorEntity) {}

